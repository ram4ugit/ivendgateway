package com.limitlessmobility.iVendGateway.model.amazonpay;

public class StroreDetail {
 
	private String storeId;
	
	private String storeIdType;
	
	private String terminalId;
	
	private String cashierMobileNo;

	public String getStoreId() {
		return storeId;
	}

	public void setStoreId(String storeId) {
		this.storeId = storeId;
	}

	public String getStoreIdType() {
		return storeIdType;
	}

	public void setStoreIdType(String storeIdType) {
		this.storeIdType = storeIdType;
	}

	public String getTerminalId() {
		return terminalId;
	}

	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}

	public String getCashierMobileNo() {
		return cashierMobileNo;
	}

	public void setCashierMobileNo(String cashierMobileNo) {
		this.cashierMobileNo = cashierMobileNo;
	}
	
	
}
